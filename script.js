let currentLang = 'en';

function setLanguage(lang) {
  const t = translations[lang];
  document.getElementById('mainTitle').textContent = t.mainTitle;
  document.getElementById('mainSubtitle').textContent = t.mainSubtitle;
  document.getElementById('projectsTitle').textContent = t.projectsTitle;
  document.getElementById('skillsTitle').textContent = t.skillsTitle;
  document.getElementById('footerText').textContent = t.footerText;
  document.getElementById('langBtn').textContent = t.langBtn;

  // Atualiza os projetos
  const cards = document.querySelectorAll('.card');
  t.projects.forEach((proj, i) => {
    if (cards[i]) {
      cards[i].querySelector('.card-title').textContent = proj.title;
      cards[i].querySelector('.card-text').innerHTML = proj.desc;
    }
  });

  // About Me
  document.getElementById('aboutTitle').textContent = t.aboutTitle;
  document.getElementById('aboutText').textContent = t.aboutText;
  document.getElementById('educationTitle').textContent = t.educationTitle;
  document.getElementById('educationText').textContent = t.educationText;
  document.getElementById('experienceTitle').textContent = t.experienceTitle;
  document.getElementById('experienceText').textContent = t.experienceText;
  document.getElementById('highlightsTitle').textContent = t.highlightsTitle;
  document.getElementById('highlightsText').innerText = t.highlightsText;

  renderSkills(lang);
}

function renderSkills(lang) {
  const t = translations[lang];
  const container = document.getElementById('skillsContainer');
  container.innerHTML = '';
  t.skillGroups.forEach(group => {
    const groupTitle = t.skillGroupTitles[group.group] || group.group;
    container.innerHTML += `<h5 class='mt-4 mb-2 text-primary'><i class='bi bi-stack'></i> ${groupTitle}</h5>`;
    group.skills.forEach(skill => {
      const label = skill.label || '';
      const skillName = skill.name;
      const progress = `<div class='mb-3'>
        <div class='d-flex justify-content-between'>
          <span><strong>${skillName}</strong></span>
          <span>${label} (${skill.level}%)</span>
        </div>
        <div class='progress' style='height: 22px;'>
          <div class='progress-bar bg-primary' role='progressbar' style='width: ${skill.level}%;' aria-valuenow='${skill.level}' aria-valuemin='0' aria-valuemax='100'></div>
        </div>
      </div>`;
      container.innerHTML += progress;
    });
  });
}

document.getElementById('langBtn').addEventListener('click', () => {
  currentLang = currentLang === 'en' ? 'pt' : 'en';
  setLanguage(currentLang);
});

// Inicializa com inglês
